	<h2>Feature Tracks: Search Tool (gene predictions)</h2>
<hr class="featuredivider" />
	
	<p><b>Instructions</b>: Use the dropdown to select a category. Then enter a value in the box and click <i>Go</i>. Any active filters will be retained. </p>
	<p><b>Searchable categories include:</b></p>
	
	<ul class="bullet1 indent2">
		<li><b>ID or Description:</b> Enter Locus or Transcript ID, or 1 or more keywords</li>
		<li><b>Locus ID:</b> Enter a locus ID, e.g. "28153.m000272".</li>
		<li><b>Scaff:</b> Enter scaffold ID, e.g. "Scaff_1".</li>
		<li><b>Scaff:From..To:</b> Type the desired scaffold ID and region in the following format: <b>Scaff_1:1000000..2000000</b></li>
		<li><b>Gene Length &#62;&#61; (kb)</b>: Enter a threshold length, e.g. "2"</li>
		<li><b>Transcript Count &#62;&#61;</b>: Enter a threshold number of transcripts at a locus</li>
		<li><b>Locus Description:</b> Enter keyword or string to be matched. No wild card needed.</li>
		<li><b>Annotation Project:</b> Enter text string. No wild cards needed. (Note- you can also use the <b>Project</b> filter.)</li>
		<li><b>yrGATE Anno ID:</b> Enter text string corresponding to a yrGATE annotation ID.</li>
		<li><b>yrGATE Gene Product:</b> Enter text string corresponding to a yrGATE gene product.</li>
		<li><b>Integrity &#60;&#61;</b>: Enter a value between 0 and 1. Results returned only for loci where Coverage &#62;&#61; Integrity.</li>
		<li><b>To remove a search query</b>, select <i>-clear search-</i> and click 'Go'</li>
	</ul>
	
<p> See also <b>Filter</b> dropdowns for specific query types that can be used instead of, or in combination with, search.</p>

