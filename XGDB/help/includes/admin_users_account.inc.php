	<h2 class="bottommargin1"> User Account Type (Community Annotation)</h2>
<hr class="featuredivider" />

	<p> Account determines what views and actions are available in <a class="help_style" href="/XGDB/help/yrgate.php">yrGATE</a> and CommunityCentral</p>

	<ul class="bullet1">
  <li> <b>USER</b> - (default) login to <a class="help_style" href="/XGDB/help/community_central.php">Community Central</a>, create, save, submit, delete own annotations. View own annotations in any GDB</li>
  <li> <b>ADMIN</b> - all of the above, plus view, curate, and delete annotations submitted by others.</li>
  <li> <b>INACTIVE</b> - login to Community Central, but NOT create save submit or delete annotations </li>
		</ul>		

<p><b>Note:</b> See also Group Accounts which assign registered users to one or more annotation groups.</p>
