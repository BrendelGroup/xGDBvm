<div class="dialogcontainer">
	<h2 class="bottmmargin1">Genome Region Submenus:</h2>

<p>These menu items affect the <b>genome region</b> currently being browsed: </p>
<hr class="featuredivider" />

	<h3>Zoom</h3>
	<ul class="bullet1 indent2">
		<li>Select appropriate genome scale under "Zoom", or click arrows to jump right or left at 50% of current scale</li>
	</ul>
	
	<h3>Annotate</h3>
	<ul class="bullet1 indent2">
		<li><b>yrGATE:</b> Opens a <a target="_blank" href="/XGDB/help/yrgate.php"> yrGATE </a>community annotation window using current genomic region.</li>
		<li><b>CpGAT:</b> Opens the <a target="_blank" href="/XGDB/help/cpgat.php">CpGAT</a> annotation tool using the current genome region.</li>
		<li><b>GenomeThreader:</b> Opens a GenomeThreader query window using current genomic region.</li>
	</ul>
	<h3>BLAST</h3>
	<ul class="bullet1 indent2">
		<li><b>BLAST - this GDB:</b> Opens a BLAST query window using current genomic region, with option to query any data from this GDB.</li>
		<li><b>BLAST - All GDB:</b> Opens a BLAST query window using current genomic region, with option to query data from any or all current GDB.</li>
	</ul>

	<h3>Download</h3>
	<ul class="bullet1 indent2">
		<li>Download aligned sequences, gene models, or complete genome annotation from this genomic region.</li>
	</ul>

	<h3>Format</h3>
	<ul class="bullet1 indent2">
		<li><b>Font Size:</b> Select a default font size for track labels.</li>
	</ul>

	<h3>Add Track</h3>
	<ul class="bullet1 indent2">
		<li><b>from GFF3:</b> Use this feature to load a local gff3 table as a new track </li>
		<li><b>from DAS:</b> Load a track from a remote DAS server (not currently implemented)</li>
	</ul>

	<h3>Nucleotide View</h3>
	<ul class="bullet1 indent2">
		<li><b>Base-pair Level</b> opens a nucleotide-level view of transcript alignments to genome (for regions &lt;10,000).</li>
	</ul>
	
