<h2 class="bottommargin1">Data Preparation</h2>

			<p>Additional steps that may be required of the user to prepare input data for processing: (see <a href="/XGDB/conf/index.php">'Config/Create -> Getting Started'</a> for details</p>
			<ul class="bullet1">
			<li>Copy or move input data to a directory under <span class="plaintext">/xGDBvm/input/</span>, and name files according to xGDBvm's naming conventions (xGDBvm will verify these file names for you)</li>
			<li>Set input file permissions to allow the Apache Web server to copy files as needed (xGDBvm will warn you if they are incorrect)</li>
			<li>Optionally, install any license keys required for data processing (see 'Config/Create' -> License Keys')</li>
			<li>Verify adequate disk storage space for the size of the input data and its expected outputs (xGDBvm does this for you after you specify your inputs) </li>
			</ul>			
