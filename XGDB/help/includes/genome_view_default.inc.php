<div class="dialogcontainer">
	<h2 class="bottmmargin1">View Default Region</h2>

<p><span class="tip_style">Click here to display a region of the genome that has been configured as "default"</span></p>

<p>Why a 'default' region?</p>
<ul class="bullet1">
	<li>Quick checks of data integrity</li>
	<li>Aid to new users learning the interface, etc.</li>
	<li>Show off the most interesting portion of the genome to users</li>
</ul>

		<p><span class="alertnotice bold">NOTE: If no default is configured, the region displayed is bp 1 to 10,000 of the LARGEST segment</span></p>

<hr class="featuredivider"/>
		<p>To <b>set or change default region</b>, do the following <span class="alertnotice"> Note: You may need an Admin account to do this</span>:</p>
	<ul class="bullet1">
		<li>Find region of interest and note scaffold ID and start, end coordinates</li>
		<li>Make sure the region is small enough that it loads to the browser in a reasonable amount of time</li>
		<li>From the GDB home page, click <i>GDBnnn &rarr; Config...</i> to access GDB Configuration page</li>
		<li>Click <i>'Edit Config'</i>, and then click submenu item <i>'Other'</i> to jump to <i>'Other Options'</i> section</li>
		<li>Edit the default values according to desired default region and click <i>'Save'</i></li> 
	</ul>


