<h2>InterProScan</h2>
<p><a href="http://www.ebi.ac.uk/interpro/">InterPro</a> is a database of protein signatures that can be used to classify proteins according to functional domains and Gene Ontology (GO) terms.</p>
<p><b>To scan:</b> First select and copy the "Your Structure" protein sequence to your computer's paste buffer, and then click <i>InterProScan</i> to open a window with the InterProScan tool.</p>
			<p>Next, paste the sequence into the query pane and click <i>submit job</i></p>	
			
			<p><b>When results are returned:</b></p>
			<ol class="bullet1">
				<li>Click <i>Raw Output</i> to view top hits in text format</li>
				<li>Copy the most relevant line(s), usually the top hit</li>
				<li>Return to the yrGATE tool and paste the InterProScan result into the "Description" window.</li>
			</ol>

