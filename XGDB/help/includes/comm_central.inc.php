	<h2 class="bottommmargin1">Community Central:</h2>
<hr class="featuredivider" />

	<p>
	Community Central is a list of all curated (approved) annotations for each GDB. The table can be accessed from the left menu bar of any genome, or from the top menu. 
	</p>
	<p><span class="tip_style">Navigation:</span>
	<br />
	GDB Home Page &rarr; Left Menu: <i>'Annotation'</i> &rarr; <i>'Community Central'</i> <br />
	Top Menu &rarr; <i>'Annotate'</i> &rarr; </i>'GDBnnn Comm Central'</i>
	</p>

	<p>Community Central shows all curated <b>community gene structure annotations</b> for this genome, in reverse chronological order. These annotations represent researchers' amendments or corrections to existing published gene models and they were created using the <a target="_blank" href="/prj/yrGATE/">yrGATE</a> annotation tool at PlantGDB.<br />
	Annotations modified in the <b>last week</b> are highlighted.
	</p>
	
