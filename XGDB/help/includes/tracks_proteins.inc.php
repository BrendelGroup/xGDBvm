	</ul>

<h3>(2) Use Search and Filters to drill down to keywords or regions of interest:</h3>
	<ul class="bullet1 indent2">
		<li><b>Search</b> by ID / Description, Genome Region (scaffold:from..to), Scaffold ID, mRNA Length </li>
		<li><b>Filter</b> by Genome Segment (Scaffold ID), Copy Number, Similarity Score, Coverage Score</li>
		<li>Searches and Filters are Boolean (AND).</li>
	</ul>
