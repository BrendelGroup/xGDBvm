		<li><b>View GenomeThreader alignment</b> by clicking the GTH icon</li>
		<li><b>Download .csv file</b> of search results or entire dataset - click link at top right.</li>
		<li><b>Modify Page View</b> using the Page controls at right.</li>
	</ul>
