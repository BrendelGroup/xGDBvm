	<h2 class="bottommargin1">Lockout Mode</h2>
	
	<p>This mode indicates that the pipeline is running on a different genome on this xGDBvm, so you will be unable to create or update and GDB until pipeline finishes.</p>
	<p>If you suspect the pipeline is stalled, and you want to start over:</p>
		<ul class="bullet1">
				<li>Access the GDB Configuration page showing <span class="Locked">Status =Locked</span> (pipeline running) </li>
				<li>Click '<span class="options_mode" >Pipeline Actions... </span>' -> <span class="abort_mode">Abort Process</span> </li>
				<li>Note that you CANNOT abort an '<span class="Update">Update'</span>process.</li>
			</ul>
	<p>Click 'Cancel' to return to Config View mode.</p>
	
