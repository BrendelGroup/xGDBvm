
<div class="dialogcontainer">
<h2 class="bottommargin1">Accessing Genome Browsers</h2>
<hr class="featuredivider" />
	<h3>From the top menu</h3>
		<ul class="bullet1">
			<li>Hover over <i>'View'</i> from the top menu, and select a genome from the dropdown.</li>
			<li>This opens a GDB Home Page with links and more information about the genome</li>
		</ul>
<hr class="featuredivider" />
	<h3>From the xGDBvm left menu</h3>
		<ul class="bullet1">
			<li>Click Left Menu -> 'View GDB &amp; Current Genomes' </li>
			<li>This opens a table of current GDBs with links to various features</li>
			<li>Click on a link icon, e.g 'Home Page' 'Blast' etc. to access that resource directly.</li>
		</ul>
<hr class="featuredivider" />
	<h3>On the GDB Home Page, you can access any genome region.</h3>
		<ul class="bullet1">
			<li>Select a genome segement and region and click 'Go' </li>
			
			<li>Use menu tools to zoom in/out, scroll left/right, or access analysis tools.</li>
			<li>Alternatively, use 'Click to view a sample region'  to view optimized view. </li>
			<li>NOTE: if no default genome region is available, you can configure one by updating the GDB Configuration File.</li>
		</ul>
