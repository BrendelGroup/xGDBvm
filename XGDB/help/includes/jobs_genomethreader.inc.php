	<h2 class="bottommargin1">HPC: GenomeThreader</h2>
            <p>GenomeThreader used it to identify high quality spliced alignments of proteins to genomic sequence.  See <a target="_blank" href="http://goblinx.soic.indiana.edu/wiki/doku.php?id=hpc">xGDBvm Wiki</a> for details.</p>
<ul class="bullet1 indent2">
<li> Source code: GenomeThreader
</li>
<li> Implemented at xGDBvm for both standalone and pipeline HPC processing
</li>
<li> Requires valid license from the developer
</li>
<li> Also available as a public app on iPlant jobs page at <a href="https://de.iplantcollaborative.org/">https://de.iplantcollaborative.org/</a>
</li>
<li> Includes wrapper script that splits genomic files (fastasplit.pl)
</li>
<li> Uses -s option with default size of 10 megabytes (parameter splitSize)
</li>
</ul>            
