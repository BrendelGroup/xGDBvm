		<p>xGDBvm includes software that requires end user licenses. Consult the <a title="Data Volumes" href="/XGDB/conf/licenses.php">Licenses</a> page to obtain licenses for:</p>
			<ul class="bullet1 indent2">
				<li><a title="Data Volumes" href="http://www.genomthreader.org/">GenomeThreader</a> for spliced alignment of proteins to genome</li>
