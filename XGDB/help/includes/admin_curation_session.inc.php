
<div class="dialogcontainer">
	<h2 class="bottommargin1">Annotation Curation Session</h2>
<hr class="featuredivider" />
	<p>As a Administrator/Curator, your responsibility is to qualify annotations that meet the standards for curation so that they can be accessed by the research public, and to provide the annotator with useful feedback that will allow him/her to improve annotation quality or correct mistakes. Below is an outline of the annotation session in xGDBvm:</p>
	<ul class="bullet1">
		<li>Click the link contained in the <b>Annotation Notification</b> email to access the yrGATE Administration Page for the appropriate GDB </li>
		<li> Alternatively, navigate to the <b> Admin</b> or <b>Group Admin</b> page for the appropriate GDB: <br />
			<span class="heading">Navigation: (top menu) 'Annotate' -> GDB00x Annotations' -> 'Admin' or 'Group Admin'.</span> <br />
			<p class="indent1"><span class="warning"> You will need to be logged in with an <b>Admin</b> account to access this page</span></p>
		</li>
		<li>At the top of the Admin page are listed all annotations SUBMITTED_FOR_REVIEW, in reverse chronological order. Choose an annotation and click the <b>Annotation ID</b> to launch a curation window
		<p class="indent1"><span class="warning"> NOTE: You may have to configure your Web browser to <b>allow popup windows</b> </span></p>
		</li>
	</ul>
		
		<p>You now have <b>30 minutes</b> to complete the curation process and submit your review before your permission lapses for that annotation.</p>
	<ul class="bullet1">

		<li>Once you <b>submit</b> the curation (ACCEPT/REJECT + comments), you will be requested to close the annotation window</li>
		<li>Now, in the far-right column of your just-curated annotation, click on the <b>Return Annotation</b> link</li>
		<li>This ends your curation session and re-sorts the curated annotation into either ACCEPTED or REJECTED category on the Admin table </li>
		<li>The annotator will receive a copy of your assessment (ACCEPT or REJECT) and your comments, via email. Admin(s) will also receive a copy of this email</li>
		<li>Rejection comments will also displayed in the yrGATE window if and when the annotator reopens the annotation to try again</li>
		<li>Acceptance comments are appended to the Description field of the annotation (where it is visible to the public).</li>
	</ul>
	
		<p>If ACCEPTED, the annotation is displayed in the Community Central list of Curated Annotations, and it also appears in Genome Context view as a green structure glyph</p>
		<p>If REJECTED, the annotator is free to modify and re-submit the annotation at a later time, or they may DELETE it.</p>

