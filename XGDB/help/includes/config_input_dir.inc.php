	<h2 class="bottommargin1">Input Data Directory</h2>
	<p>Input data files are placed in a designated <b>Input Data Directory</b></p>
	<ul class="bullet1">
		<li> First read carefully about <a title="Detailed file format and naming conventions (click ? to open new web page)" href="/XGDB/conf/data.php">filename conventions</a> for instruction on how to name files</li>
		<li>Deposit your renamed data files in a directory under <span class="plaintext largerfont">/xGDBvm/input/</span>, e.g.  <span class="plaintext largerfont">/xGDBvm/input/myData/</span> </li>
		<li>
			On the "Configure New" page, select your <b>Input Data Directory</b> from the dropdown under <b>Input Data</b>
		</li>
		<li> Once you have completed the configuration form, click <b>Save</b>.</li>
		<li> Verify that all desired filenames are on the <span class="checked">valid files</span> list</li>
		<li> During pipeline processsing, any multiple files of the same type will be concatenated together. Non-valid and missing data types will be ignored. </li>
		<li> If you have precomputed GeneSeqer (trancript) or GenomeThreader (protein) outputs, make sure their corresponding sequence file are also present</li>
	</ul>
	
				<p>Note: If you load any <b>Example</b> configuration, you will notice that input directories are configured under <span class="plaintext largerfont">/xGDBvm/examples/</span></p>
				

