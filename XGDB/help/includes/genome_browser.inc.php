	<h2 class="bottmmargin1">Genome Browsers at xGDBvm</h2>
<p>The xGDBvm Genome Browser displays a 2_D view of each genome segment and its aligned features.</p>

<hr class="featuredivider" />
	<h3 class="topmargin1">With xGDBvm you can:</h3>
	<ul class="bullet1 ">
		<li>Search and view genome features; zoom and scroll; change track order (drag and drop)</li>
		<li>See alignment details (base pair level) for all features</li>
		<li>Download sequence features by region; or download feature subregions (introns, exons, pre-mrna, etc)</li>
		<li>Run BLAST analysis (all data types) within or across genomes for any genome segment</li>
		<li>Run a local GeneSeqer, GenomeThreader or CpGAT analysis for any segement.</li>
		<li>Search for low quality annotations using GAEVAL analysis scores</li>
		<li>Re-annotate a gene model using the yrGATE tool (requires annotation account)</li>
		<li>Retain most recently viewed region between browser sessions</li>
	</ul>

