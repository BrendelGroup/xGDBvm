	<h2 class="topmargin1">Table Hints</h2>
	<ul class="bullet1">
		<li><b>For column info</b>, hover over the column heading</li>
		<li><b>To hide a status section</b>, click on the colored status row</li>
		<li><b>To edit a record</b> that has been REJECTED, SAVED, or SUBMITTED FOR REVIEW, click the <i>edit</i> link next to the annotation ID.</li>
		<li><b>To search based on a keyword, working group, etc.,</b>, enter search term and click "search". You many specify a column or search all (default).</li>
		<li><b>To sort by a column</b>, click the column header. Click again to reverse sort.</li>
		<li><b>To view an annotation record</b>, click its ID in column 1</li>
		<li><b>To view annotation in genome context</b>, click the "globe" icon in column 2</li>
		<li><b>To download annotation data </b>, click <i>M</i>, <i>P</i>, or <i>G</i> to download mRNA FASTA, protien FASTA or GFF3 formatted files, respectively. You can also <b>download all annotations</b> on this page using links at upper right.</li>
