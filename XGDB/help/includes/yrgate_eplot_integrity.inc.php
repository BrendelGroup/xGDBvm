	<h2 class="bottommargin1">GAEVAL Score</h2>

		<p><span class="alertnotice bold">GAEVAL score</span> (Annotation Integrity and Coverage) is dynamically calculated for each version of <span class="alertnotice bold">Your Structure</span> to help you maximize gene structure quality. <a title="GAEVAL documentation" target="_blank" href="/src/GAEVAL/docs/integrity.html" >Read more about GAEVAL scores here</a></p> 

	<ul class="bullet1 indent2 topmargin1">
		<li><b>Integrity Score</b> (0 to 1) is evaluated by estimating the level of annotation support which is required to provide a gene structure unlikely to be significantly altered by reannotation. </li>
		<li><b>Exon Sequence Coverage</b>  (0 to 100%) is simply from the percentage of a gene structure that is covered by evidence alignments. </li>
		<li>If Coverage is low, Integrity score will necessarily be low. However, high Coverage and low Integrity indicate that a better gene structure could be created</li>
		<li>Ideally, the Integrity and Coverage Score of <span class="alertnotice bold">Your Structure:</span> should be at least as high as the current gene model structure you are trying to improve.</li>
	</ul>

