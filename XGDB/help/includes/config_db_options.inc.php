	<h2>Data Process Options...</h2>
	<p> Click <img id='button_dp_opt' style='margin-bottom: -5px' src='/XGDB/help/images/button_dp_opt.gif' alt='button' /> to display options for data processing (see below). Click 'Cancel' to return to normal mode.</p>
	
	<p>Data Process Options may include:</p>
	<ul class="bullet1 indent2"> 
		<li><b>Validate Inputs</b> - initiates a script validates and enumerates all correctly-name files in Input directory (and Reference Protein / Repeat Mask files)</li>
		<li><b>Create Database</b> - initiates the computational pipeline that creates a new genome database and browser</li>
		<li><b>Update Database</b> - initiates the computational pipeline that updates an existing genome database and browser</li>
		<li><b>Unlock Database</b> - terminates the computational pipeline and reverts to Development status, in cases where user wants to start over.</li>
		<li><b>Drop Database</b> - removes a genome database and its online browser, but retains the configuration record</li>
		<li><b>Archive</b> - creates a restorable archive ("snapshot") of your GDB on your attached storage device</li>
		<li><b>Restore</b> - restores an archive of your GDB from your attached storage device</li>
	</ul>

