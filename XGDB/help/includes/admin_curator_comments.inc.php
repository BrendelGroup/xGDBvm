	<h2 class="bottommargin1">Curator Comments</h2>
<hr class="featuredivider" />

<p>Administrator comments are an important part of the curation process, and they are preserved as part of the annotation record</p>

	<p>(1) If you are <b>REJECTING</b> the annotation, please provide some <b> explanatory comments/suggestions </b> using the text entry box.</p>

	<ul class="bullet1">
		<li> Be sure to clearly state the reasons you rejected it, and provide any suggestions for how to overcome your objections.</li>
		<li> Your rejection text (with your username and email address) will be sent to the annotator via email</li>
		<li> This information will also displayed in the yrGATE window if and when the annotator reopens the annotation to try again</li>
		<li> Rejection comments are NOT visible to other annotators or the public, and will NOT be displayed if/when the record is accepted.</li>

	</ul>
	
	<p>(2) If you are <b>ACCEPTING</b> an annotation, you may want to add your own comments to <b>clarify the annotator's description</b>, or provide <b>additional insight</b> into the annotation.</p>
	
	
	<ul class="bullet1">
		<li> Your acceptance text (along with your username) will be sent to the annotator via email</li>
		<li> This information (along with your username) will also displayed as part of the yrGATE annotation description (appended to the annotator's description text)</li>
		<li> Acceptance comments ARE visible to the public, and become part of the annotation record.</li>

	</ul>

