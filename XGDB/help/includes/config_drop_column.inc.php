
<div class="dialogcontainer">
	<h2 class="bottommargin1">Drop GDB</h2>
	
	<p>To <b>DROP</b> a GDB (remove data tables and start over), click its <i>Drop</i> button in the column below.</p> 
	<ul class="bullet1 indent2">
        <li>Note that this will PERMANENTLY remove any output data for this GDB. It WILL NOT affect your input data, however.</li>
        <li>After a GDB is dropped, its GDB configuration will be retained, with "<span class="Development">Development</span>" status.</li>
        <li>You can now re-create a GDB by one of several methods:
            <ol class="orderedlist1">
                <li>Simply re-run the data pipeline <i>Config</i> &rarr; <i>Data Process Options</i> &rarr; <i>Create New GDB</i>, perhaps after modifying its configuration parameters (<i>Config</i> &rarr; <i>Edit Configuration</i>).</li>
                <li>Reload the GDB from its <b>Archive</b> by selecting <i>Config</i> &rarr; <i>Data Process Options</i> &rarr; <i>Restore from Archive</i> </li>
                <li>Reload the GDB from a <b>different GDB</b> <b>Archive</b> by selecting <i>Config</i> &rarr; <i>Edit Configuration</i> &rarr; <i>Use GDB Archive</i> (Dropdown), and then following Step 2 above</li>
            </ol>
        </li>
	</ul>
</div>
