	<h2>Annotation ID</h2>

	<p> This is a unique identifier for your gene annotation. Naming is AUTOMATIC based on the locus or transcript you are annotating*</p>

	<p>The automatic format (for annotations at an existing locus) is:</p>
	<ul class="bullet1">
		<li>[genome][assembly version]-[published locus name]-yrgate-[number]]</li>

		<li>For example, "<b>GDB001-gene21346-yrgate-<span style="color:red">001</b></span>"</li>
	</ul>

	<p>*For <b>new locus</b> annotations the annotation ID is derived from the genome location. </p>
	<ul class="bullet1">
		<li>For example: "<b>scaff_1:1230000-1240000-yrgate-<span style="color:red">001"</b></span></li>
	</ul>
	

