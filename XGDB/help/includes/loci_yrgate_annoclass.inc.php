	<h2>Annotations Classes</h2>
<hr class="featuredivider" />
<p>yrGATE Annotations in Community Central are classified by the annotator according to how they relate to a published gene model.</p>

	<h3> Annotation classes (as displayed in Gene Predictions table): </h3>
	<ul class="bullet1">
		<li><img src="/XGDB/images/confirm.gif" alt="confirm" />&nbsp; - Confirms existing annotation</li>
		<li><img src="/XGDB/images/extend.gif" alt="extend" />&nbsp; - Extends or trims annotation at one or both UTR ends <br /> (CDS translation unchanged)</li>
		<li><img src="/XGDB/images/improve.gif" alt="improve" />&nbsp; - Improves existing annotation structure <br /> (alters CDS translation)</li>
		<li><img src="/XGDB/images/add.gif" alt="add" />&nbsp; - Adds new variant annotation (e.g. splicing variant)</li>
		<li><img src="/XGDB/images/newlocus.gif" alt="newlocus" />&nbsp; - New locus</li>
		<li><img src="/XGDB/images/notresolv.gif" alt="notresolv" />&nbsp; - Not resolved, problematic annotation</li>
		<li><img src="/XGDB/images/delete.gif" alt="delete" />&nbsp; - Recommends deletion of an existing annotation</li>
		<li><img src="/XGDB/images/edit.gif" alt="edit" />&nbsp; - Genome edit(s) applied to one or more annotation</li>
	</ul>
	
<p>Hover over a symbol to view more information.</p>
<p>NOTE that annotations that are submitted but not approved are not available for viewing. These annotations are displayed in <b>gray</b> in the Loci/Annotations table.</p>

