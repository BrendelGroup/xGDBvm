	<h2 class="bottommargin1">Update Database</h2>
<p>Clicking the 'Update Database' button will initiate the pipeline in 'Update' mode, adding to and/or replacing existing GDB data.</p>

<p>In order to update, you must have completed the 'Update GDB' section of the GDB configuration page::</p>
	<ol class="bullet1">
	<li> Select 'Update this GDB' = 'Yes'</li>
	<li> Enter a path to update data </li>
	<li> Select one or more Update Action(s)</li>
	</ol>
</ul>
<p>Once a correct 'Update' configuration is saved, you will be able to click 'Update Database' from 'Database Options'. 
<p>Once initiated, this GDB will be in <span class="locked">Locked</span> mode until completion of the process.</p>
