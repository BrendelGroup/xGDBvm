	<h2>Protein, Transcript, Genome Datasets - </h2>

	<p><b>Protein Datasets</b> - Coding sequence (CDS) translations for each genome's annotated gene models (e.g. OSpep) </p>
	
	<p><b>Transcript Datasets</b> - The following datasets are available:</p>

	<ul class="bullet1">
		<li>
			Transcript/CDS - the predicted nucleotide sequence of the annotated gene model transcripts or coding sequene (CDS)
		</li>
		<li>
			PUT - PlantGDB-generated Unique Transcript assemblies that are splice-aligned to the designated genome.
		</li>
		<li>
			EST - Expressed Sequence Tags that are splice-aligned to the designated genome
		</li>
		<li>
			cDNA - cDNA and HTC that are splice-aligned to the designated genome
		</li>
	</ul>
	
<p><b>Genome Datasets</b> - Genomic segments from the designated genome. Those designated "genome" are pseudochromosomes; those designated "bac" are BAC or scaffold segments.</p>

	<p><b>Please note</b> that if an EST, cDNA or PUT does NOT have a spliced alignment with the designated genome, it will not be available in this BLAST dataset. If you want to BLAST all known transcript data for a species, use <a href="/cgi-bin/blast/PlantGDBblast"> PlantGDB BLAST</a> instead!</p>
	<p>The above-listed <b>FASTA Datasets </b> are all available for download from PlantGDB's <a class="hidelinkicon" target="_blank" href="http://plantgdb.org/XGDB/phplib/resource.php">Genome Browser Resources</a></p>

