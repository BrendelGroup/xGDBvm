	<h2>Working Group</h2>

	<p>If you are a member of a defined <b>Working Group</b> and this annotation is part of the group effort, select it from the dropdown.</p>
	<p>Workgroups are by <b>invitation only</b>. If you think you should be part of a workgroup but do not see it in the dropdown, please <a title="Send a note to feedback_yrGATE at PlantGDB" target="_blank" href="/utility/feedback_yrGATE.php">contact us</a>. </p>

	<p class="bold">Q: What does membership in a Working Group mean?</p>
	
	<p><b>A:</b></p>
		<ul class="bullet1">
		    <li>Working Groups are designed for use by research labs, classrooms, or annotation teams.</li>
			<li>As a Working Group member, you will be able to view annotations in progress by other Group members, on the "My Working Groups" page</li>
			<li>Working Groups can have a curator who oversees the workgroup's annotations exclusively.</li>
			<li>The Working Group assignment (if any) is listed for each annotation in Community Central, My Annotation Account, and My Working Groups, and is searchable</li>
			<li>A Working Group may encompass a single genome (for annotation teams) or all genomes at PlantGDB (for classrooms), depending on how it is set up.</li>
		</ul>
	<p class="bold">Q: How is a Working Group different from a Project?</p>
	<p>A: Working Groups are designed for teams or classrooms and are by invitation only. Projects are a free-form, user-assignable category. </p>
