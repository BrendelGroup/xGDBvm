	<h2 class="bottommargin1">Delete Most Recent GDB</h2>
	
	<p>This action <span class="alertnotice bold">irreversibly deletes</span> the latest GDB, including its database, configuration files AND output data, as well as any Archive stored under <span class="plaintext">/xGDBvm/data/ArchiveGDB/</span>.</p>

<ul class="bullet1 indent2">
	<li>Use this option <b>ONLY</b> if you want to <b>remove</b> a GDB database and don't want to save any configurations, output data, or archive.</li>
	<li>This action will NOT affect your <b>input data</b> files or other data stored on your attached volume.</li>
	<li>If you want to save the output from this GDB, use the buttons on this page to create an <b>Archive</b> and then <b>Copy</b> the archive to your Data Store, prior to using the 'Delete' option</li>
</ul>
	<p><b>* NOTE:</b> Archived data created using the <b>'Archive All GDB'</b> option will <b>NOT</b> be deleted with 'Delete Most Recent GDB'. However, if you re-archive all, this portion of the archive will be lost.</p>

