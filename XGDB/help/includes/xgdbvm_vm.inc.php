<h2 class="bottommargin1">Using a virtual machine (VM)</h2>

			<p><b>xGDBvm</b> is a virtualized server, consisting of a Linux OS platform complete with Web server, Web scripts and computational binaries in a virtual image. Beyond initial setup and assuring adequate disk storage, the user should not need to worry about server configuration or processes.</p>
			<p>There are two ways to interact with xGDBvm as a virtual web server:</p>			
			<ul class="bullet1">
			<li>Using http and/or https over an internet connection (default).</li>
			<li>Via virtual network computing (VNC) (additional configuration required).</li>
			<li>In addition, users will need ssh access to the VM in order to carry out the initial configuration and input data placement</li>
			</ul>
			<p>If accessed over http/https, we recommend using xGBDvm's password functions to limit access to configuration files</p>
			
