	<h2 class="bottommargin1">Manage yrGATE User Groups (Community Annotation)</h2>
<hr class="featuredivider" />

	<p>User Groups are convenient if you have multiple teams annotating a single or multiple genomes (GDB), as you can assign a different curator (group ADMIN) to each group. A group can cover ALL GDB or a single one.</p>

    <p><span class="heading"><b>Navigation</b>: Manage &rarr; Admin &rarr; <a href="/admin/groups.php">Manage User Groups</a> </span><span class="alertnotice">(login username/password required)</span> </p>
    
	<p class="indent1">- Use this page to create annotation <b>working groups</b> and to assign <b>group ADMINs</b> and <b>USERs</b> to these groups.</p>
	
	<p class="indent1">- Note that user group membership can be <b>global</b> in scope (GDB=ALL) or they can be specific to a single GDB.</p>

	
		<h3 class="topmargin1">Group Member Types</h3>
	<ul class="bullet1">
  <li> Group <b>USER</b> - (default) login to <a class="help_style" href="/XGDB/help/community_central.php">Community Central</a>, create, save, submit, delete own annotations. View own annotations in any GDB</li>
  <li> Group <b>ADMIN</b> - all of the above, plus view, curate, and delete annotations submitted by others <b>in this group.</b></li>
  <li> (<b>REMOVE</b> from Group) - Removes the user's Group membership, does not remove user from any other groups or from yrGATE registration </li>

		</ul>		

