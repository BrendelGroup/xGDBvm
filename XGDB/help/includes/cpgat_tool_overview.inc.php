	<h2 class="bottommargin1">CpGAT Region Tool</h2>
	<p>The <b>CpGAT Region Tool</b> can be used to annotate a genome region in an existing genome browser:</p>
	<ul class="bullet1 indent2">
		<li>Default inputs are the genome region and pre-existing transcript/protein spliced alignments<li>
		<li>Optionally, you upload your own sequence data independent of the current region</li>
		<li>The tool can also generate de novo gene structure models even if no spliced alignments are present.</li>
		<li>The CpGAT annotation output (GFF3 and FASTA files) can be used to create a CpGAT feature track or a temporary track. </li>
	</ul>
	<p>Use the CpGAT region tool if:</p> 
	<ul class="bullet1 indent2">
		<li>You don't want to annotate the entire genome </li>
		<li>You want to try out various parameters to be used later in a genome-wide annotation</li>
		<li>You want to re-annotate a particular region</li>
	</ul>
