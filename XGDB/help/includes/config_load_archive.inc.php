
<div class="dialogcontainer">
	<h2 class="bottommargin1">Load Archive (Existing GDB)</h2>
<hr class="featuredivider" />

	<p>An existing GDB can be used to <b>restore an Archive</b></p>
	<ul class="bullet1">
		<li>GDB must be in <span class="Development">Development</span> mode (not yet created, or Dropped)</li> 
		<li>First, select <i>'Edit Configuration'</i></li>
		<li><b>Select a GDB archive</b> from the dropdown list at the top of the form (if no archives are present, the dropdown does not appear).</li>
		<li>Click <i>'Load Config'</i> to create a GDB configuration that will allow you to restore this archive as your new GDB</li>
	</ul>

	<p><b>Once your Archive configuration is loaded:</b></p>
	<ul class="bullet1">
		<li>The Archive Source GDB will be indicated in the GDB name.</li>
		<li>From the GDB Config page, click <i>'Data Process Options...'</i> </li>
		<li>In Options Mode, click   <i>'Restore GDBnnn Archive'</i> to initiate data loading </li>
		<li>NOTE: Depending on data size, the restore step may take considerable time.</li>
	</ul>
	
