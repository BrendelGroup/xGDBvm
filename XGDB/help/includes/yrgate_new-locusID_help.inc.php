	<h2>New Locus ID</h2>

	<p>For annotations at a <b>new locus</b> you will need to create a unique Annotation ID: </p>
	<ul class="bullet1">
		<li>Enter a unique locus name in the <i>New Locus ID</i> text box.</li>
		<li>Suggested format is to use the chromosome location data found in Genome Location, i.e. [chr]:[start]-[end]-[gene #]-[transcript #]</li>
		<li>Then supply a unique suffix for each transcript model at that locus in the Annotation ID text box, i.e. "gene1-1", "gene2-1", etc.</li>
		<li>Example: "1:120300000-120400000-gene1-1"</li>
	</ul>
	<p>NOTE:If you create <b>more than one annotation at a new locus</b>, be sure to give each one an incremental number: gene1-1, -2, -3, etc.</p>


</div>
