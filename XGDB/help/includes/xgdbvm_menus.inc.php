<h2 class="bottommargin1">xGDBvm menus </h2>

			<p>Use the <b>Top Menu</b> to navigate anywhere on the website under four main Sections (tabbed): <b>Manage, View, Annotate</b> and <b>Help</b> </p>
			<ul class="bullet1">
			<li>The first submenu item in each tab is a <b>Getting Started</b> page for that Section (recommended for first-time users!)</li>
			<li>Some submenu items are only available when the user has added data (e.g. <i>Manage</i> &rarr; <i>Config/Create</i> &rarr; <i>GDB001</i>; <i>View</i> &rarr; <i>GDB001</i>).</li>
			</ul>
			<p>The xGDBvm <b>Left Menu</b> panel shows links related to the currently selected Section.</p>
			<ul class="bullet1">
			<li>Usually, a left menu arrow highlights the page the user is currently on</li>
			<li>A few pages (including Community Central) do not have a left menu panel and use a different top menu structure</li>
			</ul>
