<h2 class="bottommargin1">Default Display (Optional) </h2>
	<p>Sets a default genome region to display in Genome Context View, so that users can quickly get a sense of the genome content.</p>
	<p><b>To configure:</b></p>
			<ul class="bullet1 indent2">
			<li> Navigate to the <b>Other Settings</b> section of the GDB Configuration page (Either 'Create New' or in 'Edit' mode)</li>
			 <li>Enter the genome segment (e.g. scaff_3127), left coordinate and right coordinate you wish to use as the default region for GDB display.</li>		
			<li>When finished configuring, click 'Save'.</li>
			<li>Once GDB is created, you should be able to click on the 'View Default Region' link to open a Genome Context View of the configured region.</li>
 			<li>NOTE: The default region should not exceed a maximum size for rapid page loading.</li>
		</ul>
