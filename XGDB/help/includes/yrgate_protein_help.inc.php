	<h2>Putative Protein Product</h2>

	<p>(protein coding genes only) Enter a putative protein product based on blastp or other similarity-based data, ONLY IF you are confident in the similarity score and validity of the matched protein functional annotation. If unsure what to enter, or if the only hits are to unknown or "putative protein", simply leave blank.</p>

	<p>NOTE:  If the annotation includes "Similar to..." or "Putative" then include this in the entry, as it helsp signify the entry as a tertiary reference to an original functional annotation.</p>

	<p><b>Examples:</b></p>
	<ul class="bullet1">
		<li>serine carboxypeptidase 1</li>
		<li>starch synthase, putative</li>
		<li>zinc finger, C3HC4 type domain containing protein</li>
		<li>NADPH-dependent oxidoreductase</li>
		<li>Similar to RNA methyltransferase, TrmH family protein</li>
		<li>protein signal recognition particle receptor, putative</li>
	</ul>

	<p>See <a title="Genome Annotation Help - Annotation" href="/help/anno.php" target="_blank">Annotation Help</a> for more details, including a video tutorial.</p>

