<h2 class="bottommargin1">Authenticate (Log in)</h2>

			<p>Once you have registered the VM and loaded auth keys, enter your user/password and click <i>'Log in'</i></p>
			<ul class="bulletlist1 indent2">
			<li>Your <b>login status</b> will display at the top of the Web page, along with the time remaining (default is 4 hr).</li>
			<li>If your login has <b>timed out</b>, you can click <i>'Refresh'</i> to reinstate authentication for another period, as long as you haven't closed your Web browser. </li>
			<li>You can log in simultaneously from different computing devices, all of which will display the same authentication period.</li>
			<li>To end your login session on this computer, click <i>'log out'</i>. This will destroy session data locally but will not affect your login status on other devices.</li>
			<li>You can find out more about the <b>OAuth2</b> system on the <a href="http://goblinx.soic.indiana.edu/wiki/doku.php?id=job_user_authentication">xGDBvm wiki</a></li>
			</ul>
