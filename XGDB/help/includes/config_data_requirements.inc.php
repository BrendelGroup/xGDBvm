
<div class="dialogcontainer">
	<h2 class="bottommargin1">Data Requirements</h2>
	

	<h3>nucleotide FASTA sequence</h3>
<p>Acceptable and considered in translation:</p>
<ul class="bullet1 condensed indent2">
	<li><b>A</b>	Adenine</li>
	<li><b>G</b>	Guanine</li>
	<li><b>C</b>	Cytosine</li>
	<li><b>T</b>	Thymine</li>
	<li><b>Y</b>	Pyrimidine (C or T)</li>
	<li><b>R</b>	Purine (A or G)</li>
	<li><b>W</b>	weak (A or T)</li>
	<li><b>S</b>	strong (G or C)</li>
	<li><b>K</b>	keto (T or G)</li>
	<li><b>M</b>	amino (C or A)</li>
	<li><b>N</b>   any residue</li>
	<li><b>X</b>   any residue</li>
</ul>

<p>Acceptable but <span class="alertnotice">NOT</span> always considered in translation:</p>
<ul class="bullet1 condensed indent2">
	<li><b>B</b>  not A (G or C or T)</li>
	<li><b>D</b> not C (A or G or T)</li>
	<li><b>H</b> not G (A or C or T)</li>
	<li><b>V</b> not T/U (A or C or G))</li>
</ul>

	<h3>Masked residues</h3>
<p>Recognized as masked (for track purposes):</p>
<ul class="bullet1 indent2">
	<li><b>N</b>   (uppercase) - masked residue</li>
</ul>

<p><span class="alertnotice">NOT recognized</span> as masked (for track purposes)</p>
<ul class="bullet1 indent2">
	<li><b>X</b>  masked residue</li>
	<li><b>n</b> or [<b>gatc</b>] (lowercase)</li>
</ul>

   <h3>protein FASTA sequence</h3>
   <ul class="bullet1 indent2">
   		<li>Standard 1-letter amino acid symbols</li>
   		<li><b>X</b>  Any amino acid </li>
   		<li><b>B</b> = Asx = Aspartic acid or Asparagine </li>
   		<li><b>Z</b> = Glx = Glutamic acid or Glutamine</li>
   		<li><b>*</b> (star) = End or terminator</li>
	</ul>
