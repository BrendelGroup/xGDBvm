	<h2>Search</h2>

<p><span class="tip_style">The Search box, located at top of xGDB page header, allows you to quickly view a genome feature (in Genome Context or as a Record).</span></p>

	<ul class="bullet1 topmargin1">
		<li>Enter a <b>sequence ID</b> corresponding to a genome feature (a gene ID or aligned sequence ID) <b>Examples:</b>
			<ul>
				<li>AT1G01920.2 </li>
				<li>BT022034</li>
			</ul>
		</li>
		<li>Click <b>Genome</b> button to return a Genome Context view; or</li>
		<li>Click <b>Records</b> button to return a Sequence or Annotation Record</li>
	</ul>

		<p><span class="tip_style">NOTE: For batch searches and to return upstream/downstream sequence or specific gene structure components (e.g. all introns), use the <b>"Search ID/Keyword"</b> tool (see left sidebar)</span></p>

