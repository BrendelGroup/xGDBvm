	<h2 class="bottommmargin1">yrGATE: Administrate Group Annotations</h2>
	
	<hr class="featuredivider" />
	<p>
    <b>Welcome to Private Group Administration:</b> You are an administrator of annotations in one or more private groups to which annotators have also been subscribed.
    Annotations appear in reverse chronological order; the newest ones (one week or newer) are highlighted. You are responsible for curating annotations that are SUBMITTED FOR REVIEW - they will then appear as ACCEPTED or REJECTED.</p>

	<p>
	<b>How to review (curate) an annotation:</b> Click on an Annotation ID (column 2) to check it out for administration. This will grant you exclusive permission to administrate the annotation for 30 minutes. The Administration Tool will open in a new window open (NOTE- you may need to tell your browser to accept popups).
	When finished reviewing the annotation, click "Accept" or "Reject" and close the Administration Tool window. Finally, click "Return Annotation" to end your reservation session.
	</p>

    <p><b>How do private group assignments work?</b>. Users may be assigned to one or more <b>private working groups</b>, which allow group members to view all group-submitted annotations prior to approval (the <b>My Annotation Groups</b> page). Each user-group assignment may cover a single genome browser (e.g. AtGDB), several genome browsers, or ALL. You, as an administrator, can view/curate all annotations (including your own) that are submitted within working groups to which you are assigned as administrator.
    </p>
    
    <p><b>Where are my group's annotations?</b>: Annotators MUST select the correct working group when they submit their annotation, otherwise it will not appear on this list. If you are expecting additional annotations that do not appear on this list, contact annotation@iastate.edu (click "Contact Us" in upper right).
	</p>
    
