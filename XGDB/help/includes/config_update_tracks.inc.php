	<h2 class="bottommargin1">Update Data: Tracks</h2>
	<p> Under <i>Manage</i> &rarr; <i>Config/Create</i> &rarr; <i>GDBnnn Config</i>, click 'Edit' and scroll to 'Update Data'.</p>
	<p> Select track(s) to be updated under 'Update Options' configuration. Use filename guide (right hand columns) to insure each file is named appropriately; otherwise data will not be processed.</p>
	
<p>Update data and actions include:</p>
	<ul class="bullet1">
	<li>Genome sequence (append)</li>
	<li>EST, cDNA, or TSA spliced alignment (append or replace/new)</li>
	<li>Protein spliced alignment (append or replace/new)</li>
	<li>Gene annotations (.gff3 file) and their transcripts and translations (append or replace/new)</li>
	<li>CpGAT annotations (.gff3 file) and their transcripts and translations (append or replace/new)</li>
	<li>Predict genes (run CpGAT)  (append or replace/new). This will run or re-run CpGAT on current dataset</li>
	<li>Description file (ID-description, tab-delimited ) -- specify precomputed models or CpGAT-derived models</li>
	</ul>


