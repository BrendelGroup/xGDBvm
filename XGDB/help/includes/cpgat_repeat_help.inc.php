		<li><b>FASTA headers:</b> <pre class="smallerfont"> <span class="alertnotice">&gt;Sequence_ID </span>[tab or space] Description<span class="alertnotice">[return]</span></pre></li>
		<li>You may include <b>more than one</b> repeat file having the above naming conventions; these will be concatenated.</li>
	</ul>

