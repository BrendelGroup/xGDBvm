	<h2 class="bottommargin1">GDBnnn Archived:</h2>
	<p>Displays the Archive Date and the name of of the most recent Archive File associated with this GDB. </p>
	<p>If this field does not appear, then no Archive File exists for this GDB. <span class="instruction">You may want to create one! Click <i>'Data Process Options...'</i> &rarr; <i>'Create GDBnnn Archive'</i></span></p>
	<h3 class="topmargin1">How to read the Archive filename </h3> 
	
	<p>Example: <span class="plaintext">GDB001-Example-1---4-Scaffold-20140405-081055.tar</span></p>
<ul class="bullet1 indent2">
	<li><b>'GDB001'</b> - this is source GDB ID </li>
	<li><b>'Example-1---4-Scaffold-'</b> - this is GDB name that was current when the GDB was archived.</li>
	<li><b>'20140405-081055-'</b> - this is archive time stamp YYYY-MM-DD-h-m-s</li>
	<li><b>'.tar'</b> - the archived data has been converted to a tape archive (.tar) file. It is also compressed to reduce file size.</li>
</ul>



