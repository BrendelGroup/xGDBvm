	<h2>BLAST programs</h2>

	<p>Choose the appropriate blast program for the analysis you are carrying out:</p>

	<ul class="bullet1">
		<li>
			<b>blastp</b> (Protein-protein BLAST) - Given a protein query, this program returns the most similar protein sequences from the protein database that the user specifies.
		</li>
		<li>
			<b>blastn</b> (Nucleotide-nucleotide BLAST) -  Given a DNA query, this program returns the most similar DNA sequences from the DNA database that the user specifies.
		</li>
		<li>
			<b>blastx</b> (Nucleotide 6-frame translation-protein) -  This program compares the six-frame conceptual translation products of a nucleotide query sequence (both strands) against a protein sequence database..
		</li>
		<li>
			<b>tblastn</b> (Protein-nucleotide 6-frame translation) - This program compares a protein query against the all six reading frames of a nucleotide sequence database..
		</li>

	</ul>
	<p><b>Please note</b> that if an EST, cDNA or PUT does NOT have a spliced alignment with the designated genome, it will not be available in this BLAST dataset. If you want to BLAST all known transcript data for a species, use <a href="/cgi-bin/blast/PlantGDBblast"> PlantGDB BLAST</a> instead!</p>
	<p><b>*FASTA Datasets </b> are all available for download from PlantGDB's <a class="hidelinkicon" target="_blank" href="http://plantgdb.org/XGDB/phplib/resource.php">Genome Browser Resources</a></p>

