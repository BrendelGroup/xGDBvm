	<h2 class="bottommargin1">Output Directory: Download Data</h2>
	<p class="bottommargin2">The <b>Download</b> page (available for any <span class="Current">Current</span> GDB) allows users to upload any <b>output data file(s)</b> to a local drive.</p>
	
	<h3>To access</h3>
	<ul class="bullet1 indent2">
	    <li>From the <b>GDB Config</b> page, scroll down to the <i>Input/Output</i> section and click the <i>'GDBnnn Data Download'</i> link.</li>
	    <li>Or, use <b>top menu</b> to navigate to '<i>View &rarr; GDBnnn</i>'; and click <i>Resources &rarr; Download Data</i> in the left menu</li>
	</ul>

<h3>To download files</h3>
	<ul class="bullet1 indent2">
	<li>Click the desired directory to view its contents (i.e. <span class="normalfont">| download |	MySQL |	GSQOUT  |	GTHOUT  |	CpGAT  |	BLAST |</span>). Current directory is highlighted.</li>
	<li>Click the desired <b>filename</b> to launch a web browser dialog that will allow you to <b>open</b> or <b>save</b> the file to your local drive. </li>
	<li>You can also access these directories/files via <b>ssh</b> on your VM, at <span class="plaintext">/xGDBvm/data/GDB00n/data/</span></li>
</ul>

    <p>For <b>Output Directory</b> details, see <a class="help_style" title="GDB help" href="/XGDB/help/create_gdb.php#config_output_dir">Create GDB:Output Directory</a> help page</p>
	
