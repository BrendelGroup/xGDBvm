<h2>miRBASE</h2>
<p><a href="http://www.mirbase.org/index.shtml/">mirBASE</a> is a searchable database of published miRNA sequences and annotations. </p>
<p><b>To search:</b> First select and copy the "Your Structure" RNA sequence to your computer's paste buffer, and then click <i>miRBASE</i> to open a window with the miRBASE search tool.</p>
			<p>Next, paste the sequence into the query panel, select search sequences (mature vs stem-loop), search type (blastn or ssearch) and click <i>search miRNAs</i></p>	
			
			<p><b>When results are returned:</b></p>
			<ol class="bullet1">
				<li>Copy relevant text from the <ii>Hits</i> readout</li>
				<li>Return to the yrGATE tool and paste the mirBASE result into the "Description" window.</li>
				<li>IMPORTANT! Do not copy large amounts of text into the yrGATE Description field.</li>
			</ol>

