	<h2>Gene Annotation Description</h2>
<hr class="featuredivider" />
	<p>The Gene Annotation Description should provide a BRIEF overview of the annotation, for the convenience of others reviewing your contribution. <span class="warning">PLEASE DO NOT</span> paste in large amounts of BLAST output data, etc!!! <b>Description should include the following:</b></p>

	<p><b>1) What type of annotation is this?</b></p>
	<ul class="bullet1">
		<li>Corrects a published gene model (specify which one)</li>
		<li>Annotates a splicing variant at this locus</li>
		<li>Confirms an existing gene model at this locus (specify which one)</li>
		<li>Annotates a totally new locus</li>
	</ul>

	<p><b>2) What  type(s) of evidence were used in the annotation? </b></p>
	<ul class="bullet1">
		<li>cDNA</li>
		<li>EST</li>
		<li>protein</li>
		<li>ab initio, manual addition</li>
		<li>DNA sequence editing</li>
	</ul>

	<p><b>3) If this annotation corrects a published model, what features were changed?</b></p>
		<ul class="bullet1">
			<li>5', 3' termini?</li>
			<li>exon structure?</li>
			<li>split single annotation into two (gene fission)</li>
			<li>fused two annotations into one (gene fusion)</li>
		</ul>

	<p><b>4) Provide minimal description of blastp results, if appropriate</b></p>
	<ul class="bullet1">
		<li>Paste in top different-species blast hit (HEADER ONLY) including hit length </li>
		<li>Any other relevant blast information</li>
	</ul>
	
	<p>See <a title="Genome Annotation Help - Annotation" href="/help/anno.php" target="_blank">Annotation Help</a> for more details, including a video tutorial.</p>

