
<div class="dialogcontainer">
	<h2>NCBI blastp, tblastn:</h2>

	<p>Click to  open a window with NCBI's blastp or tblastn tool preloaded with "Your Structure" protein translation as the query sequence.</p>
	<ul class="bullet1">
		<li><b>blastp</b> compares your protein sequence to a protein database (default= NR, nonredundant proteins)</li>
		<li><b>tblastn</b> compares your protein sequence to a nucleotide database translated in all 6 frames (default= NT, nucleotide collection)</li>

	</ul>
	<p>Default database is NR (nonredundant proteins) or NT (nucleotides), respectively</p>
	<p><b>When results are returned:</b></p>
	<ol class="bullet1">
		<li>Review the tabular output and click the most relevant hit score to view detailed alignment</li>
		<li>Copy the most relevant line(s), usually the top hit ID, description, and sequence length</li>
		<li>Return to the yrGATE tool and paste the BLAST result into the "Description" window.</li>
		<li>DO NOT paste large amounts of BLAST data into the "Description" window.</li>
	</ol>

</div>
