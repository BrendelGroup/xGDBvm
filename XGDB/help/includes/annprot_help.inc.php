	<h2>Search Features</h2>
		<ul class="bullet1">
			<li>To <b>search by protein ID or description</b>, use the <i>Search</i> box, or click on an ID or description.</li>
			<li>For <b>wild card</b> searching: use asterix (*), e.g. <i>*kinase*</i></li>
			<li>To <b>limit the list</b> to a single scaffold or chromosome, use the <i>Search</i> box, or click on a chromosome or scaffold ID</li>
			<li>To <b>remove a search filter</b>, click <i>Clear Search</i> or <i>Refresh Table</i>.</li>
			<li>Advance to the next page using <i>Next</i></li>
			<li><b>For quick table tips, hover over any underlined link.</b></li>
		</ul>
	<h2>Links to other data views</h2>
		<ul class="bullet1">
			<li>To <b>view any alignment region in Genome Context</b>, click the icon in column 1 (opens a new window centered on the region).</li>
			<li>To view a <b>multigenome comparison of the alignment region</b>, click the icon in column 2. This will open CoGe's <a href="http://synteny.cnr.berkeley.edu/CoGe/GEvo.pl">GEvo</a> Genome Evolution Analysis tool. NOTE: if more than one model species protein alignment overlaps with the one selected, all corresponding genome regions will be loaded.</li>
			<li>To <b>export a .csv file</b>, click <i>Export Search Result</i>.</li>
		</ul>
