
<div class="dialogcontainer">
	<h3>Update GDB</h3>
	<p>To update a GDB with new data, click the 'Database Updates' heading in the Configuration file to access the Update configuration options</p>
	<ul class="bullet1">
		<li>Click 'yes' to indicate you want to update the database</li>
		<li>Under 'Update Data Path', enter a path to the update data (it should NOT be in the same directory as the original data)</li>
		<li>Under data types and actions, indicate one or more data types you wish to Append or Replace.</li>
		<li>Then click 'Save' to save the new configuration, then click 'Data Options' and 'Update Database'</li>
		<li>The database will be locked until the update pipeline is finished running. You can check progress in the View/Edit Config window for your GDB.</li>
	</ul>
