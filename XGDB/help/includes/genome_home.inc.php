	<h2>Genome Home Page</h2>

	<hr class="featuredivider" />
	<p>The Genome (GDB) Home Page provides a data summary and links to all GDB-specific views, resources and tools. <b>Scroll down</b> for categories (<i>'Navigation'</i>, <i>'Track Overview'</i>, <i>'Community Annotation'</i>, <i>'Genome Resources'</i>, <i>'Tools'</i>). The <b> left menubar</b> provides links to all genome features and tools.</p>
			<h3 class="topmargin1">To browse this genome:</h3>
		<ul class="bullet1 topmargin1">
					<li>Enter a <b>Genome Segment ID</b> and <b>Start/End coordinates</b> at top of page and click "Go".</li>
					<li>Alternatively, select an ID # from the <b>dropdown list</b> under 'Select a Segment ID' and then choose Start/End coordinates</li>
					<li>See also <i>'Click for Navigation Hints'</i> on the home page.</li>
				</ul>
			<h3>To search,</h3>
		        <ul class="bullet1 topmargin1">
					<li>Enter a sequence ID in the Search box and choose "Genome" (loads a context view) or "Records" (loads a sequence record).</li>
					<li>For multiple ID search, keyword search, and to download upstream/downstream or intron only sequences, click 'Retrieve &rarr; Search' under left menu</li>
					<li>To download any sequence from a genomic region, click 'Retrieve &rarr; Download - Region' under left menu and select region.</li>
					<li>To download ALL data used to create this genome, click 'Retrieve &rarr; Download - All' under left menu</li>
				</ul>
			<h3>To view/search tabular lists of track features,</h3>
	        	<ul class="bullet1 topmargin1">
					<li>Click under <i>'Feature Tracks'</i> in the left menubar to view <b>gene predictions</b>, <b>splice-aligned transcripts</b> and <b>proteins</b> (if any), tabulated in linear genomic order.</li>
					<li>GAEVAL (Gene Annotation EVALuation) tables for each Gene Prediction dataset provide quality scores based on actual spliced-alignment data</li>
				</ul>
			<h3>To access tools,</h3>
		        <ul class="bullet1 topmargin1">
					<li>Use links in left sidebar to access other resources and tools for analyzing the <span class="species"><?php echo $LATINORGN; ?></span> genome.</li>
					<li>Note that tools can also be accessed from a Genome Context view submenu - in this case they will use the current genome region for analysis. In contrast, the left sidebar tools are "generic" (meaning you have to specify the genome region).</li>
				</ul>
			<h3>To annotate a gene,</h3>
	        	<ul class="bullet1 topmargin1">
					<li>Browse to the region of the gene of interest, and click "Annotate this region"</li>
					<li>Search under 'Annotation &rarr; Loci  or &rarr; GAEVAL' to find low quality models </li>
					<li>For more information on gene annotation, visit <a class="help_style" title="xGDB Help Pages" href="/XGDB/help/yrgate.php">yrGATE Tool Help</a>.</li>
				</ul>
			<h3>For more resources,</h3>
		        <ul class="bullet1 topmargin1">
					<li>Click a topic on the browser left menu under <i>'Resources'</i> to view additional information</li>
					<li>Resources include Data Sources, Data Download (all output files), Pipeline Logs, and access to Configuration page (Admin only)</li>
				</ul>
