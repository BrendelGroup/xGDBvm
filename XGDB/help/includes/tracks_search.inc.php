	<h2>Feature Tracks: Search (transcript, protein)</h2>
<hr class="featuredivider" />

	<p>You can search by ID, keyword, feature length, or genomic region. The <b>default</b> is ID or Description.</p>
	<p><b>Instructions</b>: Use the dropdown to select a category. Then enter a term/value in the box and click <i>Go</i>. Other active filters will be retained. </p>
	<p><b>Searchable categories include:</b></p>
	<ul class="bullet1 indent2">
		<li><b>Any ID or Description</b> (Default) Enter ID or Description string, e.g."Scaff_2" or "Medtr4g098190.1" or "protein phosphatase". Returns all matches containing your search term.</li>
		<li><b>Scaffold ID:</b> Enter a scaffold ID, e.g. "Scaffold_1". Returns exact ID matches only.</li>
		<li>Protein or Transcript <b>ID:</b> Enter a complete or partial ID, e.g. "Medtr4g098190.1" or "Medtr4g098190" or "Medtr4g". Returns all matches containing your search term.</li>
		<li><b>Scaff:From..To:</b> Type the desired scaffold and region in the following format: <b>scaff_1:1000000..2000000</b>. Returns all proteins that align in this region.</li>
		<li><b>mRNA len &#62;&#61;</b>: Enter a threshold length (base pairs), e.g. "2000"</li>
		<li><b>Description:</b> Enter keyword or string from the Description (corresponding to the FASTA header) to be matched.</li>
	</ul>

<p><b>To clear search</b>, select "-clear search-" from dropdown and "Go", or click <i>Clear Search Filters</i></p>
<p> See also <b>Filter</b> dropdowns for specific query types ( Scaffold ID, Copy Number, Coverage, Similarity)</p>

