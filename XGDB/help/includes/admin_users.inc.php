	<h2 class="bottommargin1">Manage yrGATE Users (Community Annotation)</h2>
<hr class="featuredivider" />

	<p> The 'Manage yrGATE Users' tool lets you view and manage accounts of users who have created a <a class="help_link" href="/XGDB/help/yrgate.php">yrGATE</a> annotation account.</p>

    <p><span class="heading"><b>Navigation</b>: Manage &rarr; Admin &rarr; <a href="/admin/users.php">Manage Users</a> </span><span class="alertnotice">(login username/password required)</span> </p>
    
	<p  class="indent1">- Use this page to change <b>account type</b> for a <a class="help_style" href="/XGDB/help/yrgate.php/">yrGATE</a> user, e.g. to give a user ADMIN status.</p>
	
	<p  class="indent1">- You can also review <b>annotation numbers</b> (Submitted; Approved) for all yrGATE users, and click to view a user's curated annotations.</p>
		
	<h3 class="topmargin1">Summary of account types</h3>
	<ul class="bullet1">
  <li> <b>USER</b> - (default) login to <a class="help_style" href="/XGDB/help/community_central.php">Community Central</a>, create, save, submit, delete own annotations. View own annotations in any GDB</li>
  <li> <b>ADMIN</b> - all of the above, plus view, curate, and delete annotations submitted by others.</li>
  <li> <b>INACTIVE</b> - login to Community Central, but NOT create save submit or delete annotations (visible only in EDIT mode)</li>

		</ul>		

