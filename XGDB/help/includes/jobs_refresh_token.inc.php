<h2 class="bottommargin1">Refresh Login</h2>

			<p>Your <b>authentication token</b>is good for a limited span, but you can easly re-authenticate using the 'Refresh' feature, as long as you are in the same Web browser session.</p>
			<ol class="orderedlist1 indent2">
			<li>Navigate to any page on <i>'Manage' &rarr; 'Remote Jobs (HPC)'</i> that shows login status at the top of the page.</li>
			<li>Click 'Refresh' on the top right side.</li>
			<li>You should now be logged in for another limited span. Refresh as needed while you are checking job status or submitting jobs.</li>
			<li>Note that if you quit your Web browser or open xGDBvm on a different compute, you will need to re-authenticate using iPlant credentials.</li>
			</ol>
