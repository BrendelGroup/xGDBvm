<h2>ORF Finder</h2>
		<p>Launches an NCBI <b>ORF (Open reading Frame) Finder</b> Portal:</p>

		<ul class="bullet1">
			<li>The tool automatically analyzes "Your Structure" gene model and identifies 6-frame ORFs (green)</li>
			<li>The longest reading frame will be highlighted in purple (selected).</li>
			<li>If an alternative ORF is desired, click to select it.</li>
			<li>Finally, click the yellow box "Select ORF for Annotation" to add this ORF to the gene model.</li>				<li>The gene model will now display start and stop glyphs.</li>
			<li>The protein window will now display the protein sequence.</li>
		</ul>
		<p><b>IMPORTANT:</b> Make sure the ORF contains both start (M) and stop (*) codons. An ORF that extends to one or both ends of the sequence may lack these features -- if so, it must be considered a truncated ORF.</p>
</div>

