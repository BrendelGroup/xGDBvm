	<h2>Manual Entry</h2>

	<p>You can add your own exon structures using the "Manual Entry" feature.</p>

	<p>For example, if two partial exons in the Evidence Plot each have one legitimate exon/intron junction, you can enter the combined left/right coordinates to create a new exon.</p>

	<ul class="bullet1">
		<li>Enter starting exon coordinate in the <i>Start</i> box</li>
		<li>Enter ending exon coordinate in the <i>End</i> box</li>
		<li>Click <i>Add</i></li>
		<li>The new coordinates will be added to "Your Structure"</li>
		<li>The coordinates are also displayed in the "User Defined Exons" window</li>
		<li>To unselect an exon, click the radio button in the "User Defined Exons" </li>
	</ul>
<p><span class="attention_text">Note-</span> if you use Manual Entry to add coordinates identical to an exon in the Evidence Table, you will not be able to add them. Instead, select it from the Evidence Table.</p>
	<p>See <a title="Genome Annotation Help - Annotation" href="/help/anno.php" target="_blank">Annotation Help</a> for more details, including a video tutorial.</p>

