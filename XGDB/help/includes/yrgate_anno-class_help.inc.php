	<h2>Annotation Class</h2>

	<p>Select the option that describes how your re-annotation affects the published transcript model:</p>

	<ul class="bullet1">
		<li><b>Confirm</b>  - Select if you are simply confirming that a gene model is correct given available evidence</li>
		<li><b>Improve</b> - Select if you are correcting or improving an existing gene model's exon structure and creating a different open reading frame</li>
		<li><b>Variant</b> - Select if you are annotating a new splicing isoform or variant at an existing locus</li>
		<li><b>New Locus</b> - Select if you are annotating a new locus not represented by published models (it will be named automatically)</li>
		<li><b>Extend/Trim</b> - Select if you are simply extending or trimming a UTR region, with no change to open reading frame</li>
		<li><b>Delete</b> - Select if you are recommending a gene model be deleted from the dataset</li>
		<li><b>Not Resolved</b> - Select if the gene model is inconsistent with evidence but you are unable to improve it.</li>
	</ul>
	
	<p>Once you have made this selection, you will choose the specific locus and transcript ID that your annotation is intended to modify.</p>
	
	<p>See <a title="Genome Annotation Help - Annotation" href="/help/anno.php" target="_blank">Annotation Help</a> for more details, including a video tutorial.</p>

