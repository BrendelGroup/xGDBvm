	<h2 class="bottommargin1">Pipeline Error Log</h2>
		 
	<p>Any ERRORs in the <b>pipeline</b> are logged separately in a flat file, <span class="plaintext normalfont">Pipeline_error.log</span>, stored under the GDB directory <span class="plaintext normalfont">/xGDBvm/data/GDB00n/logs/</span></p>

	<p>How to view the Error Log:</p>
	<ul class="bullet1 indent2">
		<li>Errors (if any) are listed at the top of the <b>Config</b> page under: <span class="alertnotice bold">Pipeline Errors Exist </span><span class="heading" >(Click to view details)</span> 	</li>
		<li>Click the <img alt="" src='/XGDB/images/help-icon.png' /> next to any error for suggestions on how to remediate the error.</li>
		<li>Also on the <b>Config</b> page, click where you see the Error <img alt="" src="/XGDB/images/logfile-icon.png" /> magnifying glass to view <b>error log</b> as a popup or a full web page.</li>
		<li>Each GDB Home Page <b>Left Menu</b> includes a link to a full page version under <b>Logs</b></li>
	</ul>
		<p>The Error Log (full page version) also contains links to <b>other pipeline log files</b>:</p>
			<ul class="bullet1 indent2">
				<li><a href="/XGDB/help/create_gdb.php#config_pipeline_cpgat_log">CpGAT Log</a> - detailed output for each genome segment annotated (optional)</li>
				<li><a href="/XGDB/help/create_gdb.php#config_pipeline_procedure_log">Procedure Log</a> - a list all steps completed in the pipeline procedure (including errors)</li>
			</ul>
	
