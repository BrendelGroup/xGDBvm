	<h2>Portals</h2>

	<p>You can add additional exon structures to your annotation, identified through a Gene Finder portal.</p>

	<p>The portals available throught yrGATE are described below:</p>

	<ul class="bullet1">
		<li><b>CpGAT at PlantGDB</b>- Comprehensive plant Genome Annotation Tool. CpGAT takes into account both splice-aligned sequence and ab initio gene models to compute gene models for this region (results take several minutes).</li>
		<li><b>GeneSeqer at PlantGDB</b> - spliced alignment tool that computes exon-intron junctions based on transcripts. You can use this tool if you have additional transcript data from this region.</li>
		<li><b>GeneMark</b> - an ab initio genefinder for eukaryotic sequences. Runs automatically to find exons for this genome region. Click to add exons to "Your Structure"/</li>
		<li><b>GENSCAN</b> - an ab initio genefinder for eukaryotic sequences. Runs automatically to find exons for this genome region. Click to add exons to "Your Structure"/</li>

	</ul>

	<p>See <a title="Genome Annotation Help - Annotation" href="/help/anno.php" target="_blank">Annotation Help</a> for more details</p>

