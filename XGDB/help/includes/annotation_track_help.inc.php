	</ul>

<p>What does menu item <i>Show GAEVAL Flags</i> mean?</p>
	<ul class="bullet1">
		<li>GAEVAL flags are a way of describing the degree of confidence in a gene model based on available transcript evidence</li>
		<li>To turn on, select <i>Show GAEVAL Flags</i> from this menu. The flag glyph will be displayed above the track glyph</li>
	</ul>

