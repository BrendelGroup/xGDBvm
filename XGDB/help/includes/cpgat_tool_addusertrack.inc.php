<div class="dialogcontainer">
	<h2 class="bottommargin1">Add User Track</h2>
	<p>This features lets you view your annotation data in a temporary track.</p>
	<p> <b> To try out the User Track in the CpGAT tool</b>:</p> 
	<ol class="orderedlist1 indent2">
		<li>Run Example 3 under Manage->Configure->Create New GDB (or use your own GDB data and region)</li>
		<li>Go to the 'default region' (or any genic region) in your GDB genome browser</li>
		<li>Select Annotate->CpGAT in the orange dropdown menu</li>
		<li>The genomic region should load. Run region CpGAT tool using defaults</li>
		<li>Download to your local drive one of the ~.gff3 output files (click it and save from your web browser).</li>
		<li>Click 'Add User Track' button under Option 1 to open an orange popup dialog with a tab 'GFF3'</li>
		<li>Click 'Browse' and select the local file to upload. Name the track and select the correct genomes segment from dropdown.</li>
		<li>Complete the process and then return to the genome browser to view your new User Track (orange track glyph).</li>
		<li>Click the 'X' to remove the track from your genome browser</li>
	</ol>
