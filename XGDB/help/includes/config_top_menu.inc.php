		<h2>Config Top Menu</h2>
	<ul class="bullet1 indent2">
		<li>Click an item on the <b>Config Top Menu</b> to jump to a configuration section</li>
		<li>The first sections are for naming your GDB and setting data paths and parameters for creating your GDB, e.g.
			<ul class="bullet1 indent2">
				<li> <b>General</b> section is for database name (required), organism and genome type</li>
				<li> <b>I/O</b> is for specifying input data path (required) and viewing output files (if Current GDB)</li>
				<li><b>Transcript, Protein</b> display optional parameters for spliced alignment, including HPC option</li>
				<li><b>Gene Prediction</b> contains parameters for the optional CpGAT gene prediction subroutine</li>
				<li><b>Update</b> section can be used to specify input data and actions to modify a Current GDB.</li>
				<li><b>Other</b> other parameters, e.g. default segment, admin email.</li>
			</ul>
		</li>
		<li>The last four sections (<b>Genomes, Segments, Models, Alignment</b>) allow for user-provided metadata as well as displaying genome statistics for Current GDB.
			<ul class="bullet1 indent2">
				<li><b>Genome</b> user-provided data about this genome</li>
				<li><b>Scaffolds</b> user-provided and output data about genome scaffolds</li>
				<li><b>Gene Models</b> user-provided and output data about proteins used for spliced alignment</li>
				<li><b>Spliced Alignments</b> user-provided ouitput data about transcripts used for spliced alignment</li>
			</ul>
		</li>
	</ul>
