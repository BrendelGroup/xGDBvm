	<h2>Annotation Projects</h2>
<hr class="featuredivider" />

<p>Projects are informal locus categories intended as an aid to annotators. Use the <b>Projects</b> page to find a project of interest, and then click to view details.</p>

<p><b>Navigation:</b> View [select GDB] &rarr; left menu &rarr; Annotation &rarr; Loci (pre-configured or CpGAT) &rarr; View Project Information Page</p>

<ul class="bullet1">
		<li>Projects may refer to a pathway, function, phenotype or other category</li>
		<li>A locus can belong to one or more projects, or no projects.</li>
		<li>yrGATE annotations can be assigned to only one project or no project.</li>
	</ul>

<h3>Total Loci -</h3>
<ul class="bullet1">
	<li>Loci associated with each project (total count)</li>
	<li>Click to view project entries in Loci/Annotations table
</ul>
<h3>Low Quality Loci -</h3>
<ul class="bullet1">
	<li>Loci with Average Coverage > .75 AND Average Integrity < .75 (i.e. most likely to be in need of re-annotation)</li>
	<li>Click to view these entries in Loci/Annotations table
</ul>
<h3>Loci With Annotations</h3>
<ul class="bullet1">
	<li>Loci associated with each project that have one or more annotations</li>
	<li>Click to view annotated project entries in Loci/Annotations table
</ul>
<h3>Annotations in each class</h3>
<ul class="bullet1">
	<li><b>Total</b> - All transcript annotations for each project. Since projects overlap, some annotations may not appear under a Project it is associated with.</li>
	<li>Click to view all (curated) annotations in Community Central.</li>
	<li>Annotation Classes (symbols)- The project annotations broken down by class. Hover over symbol for explanation or click "?"</li>
</ul>		
	
