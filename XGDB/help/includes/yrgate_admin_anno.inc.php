	<h2 class="bottommmargin1">yrGATE: Administration Annotations<span class="heading"></h2>
	
	<hr class="featuredivider" />

    <p><b>Welcome to Annotation Administration!</b> You are an administrator of gene structure annotations created via the yrGATE tool.
    Annotations appear in reverse chronological order; the newest ones (one week or newer) are highlighted. You are responsible for curating annotations that are SUBMITTED FOR REVIEW - they will then appear as ACCEPTED or REJECTED.</p>

	<p>
	<b>How to review (curate) an annotation:</b> Click on an Annotation ID (column 2) to check it out for administration. This will grant you exclusive permission to administrate the annotation for 30 minutes. The Administration Tool will open in a new window open (NOTE- you may need to tell your browser to accept popups).</p>
	<p>
	When finished reviewing the annotation, click "Accept" or "Reject" and close the Administration Tool window. Finally, click "Return Annotation" to end your reservation session.
	</p>

