<h2 class="bottommargin1">Set Up xGDBvm</h2>

			<p>Users first log in to the VM using SSH, and the follow directions found in <span class="plaintext largerfont">/xGDBvm/0README-iPlant </span> to set up an xGDBvm instance</p>
			<p>The setup steps accomplish the following (See also xGDBvm Wiki for details)</p>				
			<ul class="bullet1">
				<li>Updates the VM to the latest code</li>
				<li>Mounts external volumes to the VM as a source of input and output data</li>
				<li>Sets up the directory environment and database location required for processing.</li>
				<li>Sets directory permissions/ownership to allow the Apache Web server to copy files as needed</li>
			</ul>
