     <h2 class="bottommargin1">Search ID/Keyword (Advanced Search)</h2>

	 <p><span class="tip_style"><b>Navigation</b>: GDB Home &rarr; Left Menu &rarr; Search/Retrieve &rarr; By ID/Keyword</span></p>

		<p><b>What it does:</b> Search by ID or text, batchwise or boolean. From the results window you can retrieve upstream/downstream sequences, unspliced/spliced transcript, CDS, or introns only</p>
<hr class="featuredivider" />
		 <h3>To search using identifiers (IDs),</h3>
             <ul class="bullet1">
				<li>enter the ID or a list of IDs separated by spaces or by line feeds</li>
				<li>Alternatively, upload a file containing a list of IDs.</li>
				<li>Click <i>'Search'</i> or alternatively, <i>'Limit Search Scope'</i> for more options</li>
				<li>Click for generic examples (above search box)</li>
				<li>Actual <b>Sample Input</b> is also provided: Click link at bottom of page, and copy an ID for searching.</li>
			</ul>
		 <h3>Wild card searches:</h3>
            <ul class="bullet1">
				<li>Examples: AC%035 or AC*035</li>
				<li>Single character: AC3(.)035 or AC3(_)035 </li>
			</ul>
		<h3>To search using a text word or phrase, </h3>
            <ul class="bullet1">
				<li>Use double quotes ("") around the text </li>
				<li>Multiple text search items should be separated with a space (for a boolean AND search), or by line feeds (for a boolean OR search).</li>	
				<li>For example: "Encodes a Dicer homolog"  "globular-elongate" returns only entries containing both phrases. Text terms are NOT case-sensitive.</li>
			</ul>
		<p>Optionally, <b>click appropriate box</b> to return all sequences that overlap in the genome with the query ID(s), or to return only a sequence record.</p>
		<h3>Click <i>'Go'</i> to view search results.</h3>
            <ul class="bullet1">
				<li>From the results page you can select among additional options for sequence retrieval, including flanking sequences</li>
				<li>(NOTE: To speed up searching, you may specify input data types, such as EST, cDNA, PUT or Probe. If they are combined or you don't know, use default "ALL")</li>
			</ul>
