	<h2 class="bottommargin1">GDBnnn Restored:</h2>
	<p>Displays the Restore Date and the name of the Archive File used to restore this GDB.</p>
	<p>This File can be either:</p>

<ul class="bullet1 indent2">
    <li>A previously-archived version of this GDB
	</li>
	<li>An archive from a different GDB (if you previously configured this GDB to restore it)</li>
</ul>
	
	<h3>How to read the Archive filename </h3> 
	
	<p>Example: <span class="plaintext">GDB001-Example-1---4-Scaffold-20140405-081055.tar</span></p>
<ul class="bullet1 indent2">
	<li><b>'GDB001'</b> - this is source GDB ID </li>
	<li><b>'Example-1---4-Scaffold-'</b> - this is GDB name</li>
	<li><b>'20140405-081055-'</b> - this is archive time stamp YYYY-MM-DD-h-m-s</li>
	<li><b>'.tar'</b> - the archived data has been converted to a tape archive (.tar) file. It is also compressed to reduce file size.</li>
</ul>



