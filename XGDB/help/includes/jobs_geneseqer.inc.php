	<h2 class="bottommargin1">HPC: GeneSeqer</h2>
            <p>GeneSeqer is used to identify high quality spliced alignments of transcript (EST, cDNA, TSA) to genomic sequence.  See <a target="_blank" href="http://goblinx.soic.indiana.edu/wiki/doku.php?id=hpc">xGDBvm Wiki</a> for details.</p>
<ul class="bullet1 indent2">
<li> Source code: GenomeThreader (MPI version)
</li>
<li> Implemented at xGDBvm for both standalone HPC and pipeline HPC processing
</li>
<li> Also available as a public app in the iPlant Discovery Environment (<a href="https://de.iplantcollaborative.org/">https://de.iplantcollaborative.org/</a>)
</li>
<li> Includes wrapper code to split genomic sequences into smaller multi-fasta (uses fastasplit.pl)
</li>
</ul>

