	<h2>yrGATE (Your Gene Annotation Tool for Eukaryotes)</h2>
	<hr class="featuredivider" />
	<p>yrGATE is a Web-based tool that allows you to create <b>gene annotations</b> that correct or amend existing transcript structures. Each submitted annotation is curated and published at PlantGDB and <i>via</i> DAS. All you need is an <b>annotation account</b> at PlantGDB.</p>

	<h2>The yrGATE tool in a nutshell:</h2>
		<ul class="bullet1">
			<li>The <b>Evidence Plot</b> shows splice-aligned transcript and protein evidence and current gene models.</li>
			<li><b>Create an annotation</b> ("Your Structure") by clicking the appropriate evidence alignment IDs or exons.</li>
			<li>Check the quality of your exons in the <b>Evidence Table</b>. Then <b>find an ORF</b> and run <b>BLAST</b> to verify that your model is believable.</li>
			<li>Finally, enter additional data (Annotation Class, Description, etc.), and click <b>Submit</b> or <b>Save</b></li>
		    <li>All <b>submitted</b> or <b>saved</b> annotations are displayed (for the logged-in user only) in the xGDBvm genome browser</li>
			<li>Submitted annotations can be <b>curated</b> by authorized admin and, once accepted, are displayed publically in the xGDBvm genome browser.</li>
		</ul>
	
	<p>Use the <b>Quick Start Guide</b> on the yrGATE tool page to review annotation steps in more detail. See also <a title="Help and Tutorials" href="/src/yrGATE/index.php">yrGATE Overview</a></p>
	
