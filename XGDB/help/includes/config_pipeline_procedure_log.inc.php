	<h2 class="bottommargin1">Pipeline Procedure Log</h2>
		 
	<p>Each step in the <b>pipeline</b> is logged in detail in a flat file, <span class="plaintext normalfont">Pipeline_procedure.log</span>, stored under the GDB directory <span class="plaintext normalfont">/xGDBvm/data/GDB00n/logs/</span></p>

	<p>How to view the Procedure Log:</p>
	<ul class="bullet1 indent2">
		<li>During pipeline processing, the most recent step is visible as <b>Now Processing</b> at the top of the <span class="Locked">LOCKED</span> Config page</li>
		<li>Click where you see the <img alt="" src="/XGDB/images/logfile-icon.png" /> magnifying glass to view logfile details as a popup or a full web page.</li>
	</ul>
		<p>The Pipeline Procedure Log (full page version) contains links to <b>other pipeline log files</b>:</p>
			<ul class="bullet1 indent2">
				<li><a href="/XGDB/help/create_gdb.php#config_pipeline_cpgat_log">CpGAT Log</a> - detailed output for each genome segment annotated (optional)</li>
				<li><a href="/XGDB/help/create_gdb.php#config_pipeline_error_log">Error Log</a> - a list of any ERROR logs from the pipeline procedure, with links to help for each</li>
				<li>Each GDB Home Page <b>Left Menu</b> includes a link to a full page version under <b>Logs</b></li>
			</ul>
		<p><span class="tip_style">Helpful Tip: The Procedure Log also includes links to output data directories (once pipeline is completed)</span></p>

