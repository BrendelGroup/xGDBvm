	<h2 class="bottommargin1">yrGATE Curation - Email Notifications</h2>
<hr class="featuredivider" />

	<p>When a user <b>submits a yrGATE annotation for curation</b>, an email is sent by the Apache webserver to the appropriate Admin email(s), depending on how this has been configured. xGDB prioritizes Admin notifications as follows:</p>
	
	<ul class="bullet1" style="list-style:none">
	
		<li> <b>(1) Working Group Admin(s)</b> are notified, if the annotation is associated with a Working Group.
		<br /><span class="heading">(Configured in 'Manage' -> 'Admin' -> 'Manage User Groups')</span></li>
		<li><b> (2) Global yrGATE Admin(s)</b> are notified, if the annotation is <b>not</b> associated with a Working Group and <b>no</b> GDB Admin is configured.
		<br /><span class="heading">(Configured in 'Manage' -> 'Admin' -> 'yrGATE Admin Email')</span></li>
	</ul>
	<p>Second, when an Administrator <b>curates a submitted yrGATE annotation</b>, an email is sent to (1) the <b>original annotator</b> and the appropriate <b>Admin email(s)</b>, prioritized as above</p>

