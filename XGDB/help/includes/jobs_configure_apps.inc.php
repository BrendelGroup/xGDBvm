
<div class="dialogcontainer">
		<h2 class="bottommargin1">Configure Apps</h2>
		<p>You may need to configure or update the HPC Apps used by xGDBvm-iPlant (e.g. for launching Geneseqer-MPI on multiple cores). </p>
			<ol class="orderedlist1">
				<li>Navigate to <i>'Manage'</i> &rarr; <i>'Remote Jobs'</i> &rarr; <i>'<a title="Configure for Remote Jobs" href="/XGDB/jobs/apps.php">Configure Apps</a>'</i> </li>
				<li>Click 'Add New App' and enter App ID and all metadata as appropriate, then click 'Insert'.</li>
				<li>With multiple apps for a program, one is designated as 'default'; to change 'default' app, click 'D' in the appropriate column.</li>
				<li>Apps can be removed from the list by clicking 'X' in the appropriate column; to remove a 'default' app, first assign a different app as 'default'.</li>
	        </ol>
		<p>With App IDs in place, correct API configuration, and VM-mounted Data Store, your VM should be ready to submit HPC jobs, either as standalone jobs or as part of a GDB pipeline.</p>
