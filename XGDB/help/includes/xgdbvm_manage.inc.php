<h2 class="bottommargin1">Manage</h2>

			<p><b>Manage</b> is the section dedicated to <b>setting up your VM</b>, <b>creating genome browsers</b>, and <b> managing external jobs</b>. <span class="warning"> NOTE: these pages may be password-protected</span></p>
			<p> Under the <i>Manage</i> menu heading:</p>				
			<ul class="bullet1">
			<li>Click <i>'Admin</i> to name your VM, set up passwords, and/or administer yrGATE (Community Annotation) users and administrators</li>
			<li>Click <i>'Configure/Create '</i> to start the process of creating a new genome data broker (GDB), or updating an existing one.</li>
			<li>Click <i>'Remote HPC '</i> to set up or manage remote (high performance) compute jobs (iPlant only)</li>
			</ul>
		<p><b>Password protection</b> (under <i>Manage</i> &rarr; <i>Admin</i> &rarr; <i>Set up Passwords</i>) is highly recommended if your site is going on the open internet. That way, only valid users will be able to access the above functions.</p>
