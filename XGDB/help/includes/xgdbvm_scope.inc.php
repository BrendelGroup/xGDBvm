<h2 class="bottommargin1">Scope and functions</h2>

			<p><b>xGDBvm</b> allows users to create a genome browser (GDB), complete with spliced alignment of transcript/protein data and optional genome annotation, using a fully configured virtual server. </p>
			<ul class="bullet1">
			<li>To create a GDB, users place their input data in a directory on or attached to the VM, set a few configuration parameters, and then press "Run". </li>
			<li>When complete, the data can be browsed, searched, or even re-annotated in a full-featured genome browser.</li>	
			<li>Multiple genomes can be displayed in a single VM, and data logging/archiving is standard. xGDBvm also can be configured to use external compute resources to speed process time (iPlant only).</li>
			</ul>
		<p><b>xGDBvm</b> is currently implemented at iPlant's <a href="http://www.iplantcollaborative.org/discover/atmosphere">Atmosphere</a> environment for cloud computing.</p>
