	<h2>Feature Tracks: Coverage Score (transcript, protein)</h2>
<hr class="featuredivider" />

<p><b>Coverage Score</b> indicates the degree to which spliced alignment evidence (transcript or protein back-translation) matches the target sequence. </p>
	<ul class="bullet1 indent2">
		<li>A Coverage Score of 1.0 indicates that the entire transcript (or back-translated) sequence was aligned to the genome</li>
		<li>A lower score indicates that some portion of the sequence did not align to the genome.</li>
		<li>Note that xGDBvm does not display spliced alignments with coverage below a certain threshold (usually 0.8)</li>
	</ul>
<p>Use the <b>'Coverage Score' filter </b> to select all entries above or below the threshold selected in the dropdown.</p>
