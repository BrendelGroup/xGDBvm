	<h2>Feature Tracks: Similarity Score (transcript, protein) </h2>
<hr class="featuredivider" />

<p><b>Similarity Score</b> indicates the degree to which spliced alignment evidence (transcript or protein back-translation) matches the target sequence, over the region that showed a match (see also Coverage). </p>
	<ul class="bullet1 indent2">
		<li>An Similarity Score of 1.0 indicates <b>perfect match</b> between aligned sequence and genome, over the matching region.</li>
		<li>A lower Similarity Score indicates some degree of <b>divergence</b> in sequence over the matching region.</li>
	</ul>
<p>Use the 'Similarity Score' filter to select all entries above or below the threshold selected in the dropdown.</p>
