	<h2 class="bottommmargin1">GAEVAL Analysis of Transcripts</h2>
	<span class="heading indent2"> Navigation: (GDB left menu <i>Feature Tracks</i> &rarr; <i>GAEVAL scores</i>)</span>
<hr class="featuredivider" />
		<p>This page displays incongruencies in <?php echo $LATINORGN; ?> gene models, based on splice-aligned transcript evidence overlapping with a locus. </p>
		<p>GAEVAL data is categorized by the type of incongruency, for example alternative splicing, and can be very helpful for choosing genes to re-annotation via yrGATE.</p>

	        <h2 class="topmargin1">Quick Start: Find "problematic" annotations</h2>
				<ul class="bullet1">
				<li>FIRST, Select annotation source from dropdown and click "Retrieve Annotations" to retrieve default annotations (the first 50) in tabular form.</li>
				<li>THEN, Click one of the categories listed in red below under, e.g. "MOST WANTED! Annotations", to view a list of models flagged for annotation problems.</li>
				<li>The data columns and evidence flags allow you to quickly assess the status of each gene model.</li>
				<li>Then click the URL "globe" link to view the gene model and its transcript alignments in a genome browser window. Now you can use the yrGATE tool to annotate the locus.</li>
				</ul>
				<p><b>For more information:</b> Go to the <a title="Community Annotation Help" href="/XGDB/help/gaeval.php">GAEVAL</a> help pages. For help annotating genes with yrGATE, visit <a title="Community Annotation Help" href="/XGDB/help/yrgate.php">yrGATE</a> help pages.</p>
				<h2>What do the symbols mean?</h2>
				<ul class="bullet1">
					<li><img alt="GAEVAL Symbol Example" class="align" src="/XGDB/images/Flags/icon_fission.png" /> The GAEVAL types and their symbols are described in <a title="Community Annotation Help" href="/XGDB/help/anno.php#GAEVALTypes">Annotation Help</a>.</li>
					<li><img alt="Evidence Flag Example" class="align" src="/XGDB/images/Flags/GvlRedTarg.jpg" /> The evidence flags (target symbols) are explained in <a title="Community Annotation Help" href="/XGDB/help/anno.php#GAEVALEvidence">Annotation Help</a>.</li>
				</ul>
                <h2>Other Display and Search Options</h2> 
				<ul class="bullet1">
				<li><b>Display Options:</b> Click checkboxes to customize the columns that will display when you view a list of gene models. You can also specify column order using the dropdowns.</li> 
				<li><b>BEST OF SHOW! Annotations:</b> This window displays gene models previously annotated by other yrGATE users, organized by category.</li>
				<li><b>MOST WANTED! Annotations:</b> This window displays all GAEVAL-flagged gene models by category (Alternative Splicing, Alternative Transcript Termination, Gene Fission,Gene Fusion, Erroneous Gene Overlap)</li>
				<li><b>Annotation Filters:</b> These checkboxes allow you to customize the display of gene models according to the criteria you set. After setting parameters as desired, click "Retrieve Annotations" at the top of the page to view your filtered list</li>
				</ul>

