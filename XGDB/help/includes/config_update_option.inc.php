	<h2 class="bottommargin1">Update xGDBvm Database</h2>
	<p>xGDBvm lets you append or replace existing data in your genome browser. Take the following steps:</p>
	<ol class="bullet1 indent2">
	<li>In <b>'Edit'</b> Mode, scroll to the <b>'Updates Data &amp; Actions'</b> section</li>
	<li> Select  <b>'Update this GDB?' ='Yes'</b> </li>
	<li> Enter an <b>Update Data Path</b> where you plan to place data files for update. </li>
	<li> Determine the <b>Update Action(s)</b> for the data type(s) you want to update (EST/cDNA/Transcript Assembly, Genome Segments, Proteins, or Gene Models: click 'Append' or 'Replace/New' for each data type. Default='None'.</li>
	<li><b>'Save'</b> your configuration changes</li>
	<li> Place data files in the <b>Update Data </b> directory you specified in the Configuration. Read carefully about <a target="_blank" title="Detailed file format and naming conventions (click ? to open new web page)" href="/XGDB/help/create_gdb.php#config_file_names">filename conventions</a> and make sure each file is named appropriately; otherwise data will not be processed.
	</li>
	<li> Now click <b>'Database Options...' &rarr; 'Update Database'</b> to start pipeline.</li>
	</ol>
	<p> Once complete (from less a minute to many hours, depending on genome size and number of alignments), the new genome will added to the Genomes menu. Click "Cancel" to return to configuration view.</p>


