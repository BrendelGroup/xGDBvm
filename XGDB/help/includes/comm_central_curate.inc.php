	<h2 class="bottommmargin1">Curating Annotations</h2>
<hr class="featuredivider" />
	<p>
Annotations are curated by first accessing the "Admin" page at Community Central (you must be logged in). </p>
<p>All submitted annotations are listed here as <b>SUBMITTED_FOR_REVIEW</b>.As an ADMIN, you can "check" out any annotation, review it, and approve or reject it. The submitter will receive email confirmation of your actions</p>
	</p>
	
    <p><span class="tip_style">Navigation</span>: GDB Home Page &rarr; Left Menu: Annotation &rarr; Community Central &rarr; Admin </p>

	<h3 class="topmargin1">Curation Steps</h3>
	<ul class="bullet1">
		<li>Check out an annotation by clicking<b>Annotation ID</b>. <span class="alertnotice"> You may have to configure your browser for popups for this step</span></li>
		<li>The "Check Out" process gives you exclusive access to this record for 30 minutes. Be sure to complete annotation in this time or you will lose access.</li>
		<li>Review the annotation for completeness and correctness give the available evidence (see <a href=\XGDB/help/community_central.php#comm_central_curationtips">curation tips</a> </li>
		<li>Enter message in the explanation box (or leave blank whick will send a standardized message), and click "Submit"</li>
		<li>Close yrGATE tool window as instructed by the confirmations screen</li>
		<li>On the Admin page, click "Return Annotation"</li>
		<li>If you don't complete the curation process, just close the window and click  "Return Annotation". This will allow another annotator access to the same record.</li>


	</ul>
