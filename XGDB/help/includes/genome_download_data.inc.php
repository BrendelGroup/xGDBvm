	<h2 class="bottommargin1">Download Data</h2>
	<p class="bottommargin2">The <b>Download</b> page (available for any <span class="Current">Current</span> GDB) allows users to upload any <b>output data file(s)</b> to a local drive.</p>
	
	<h3>To access</h3>
	<ul class="bullet1">
	    <li>From the <b>top menu</b> navigate to '<i>View &rarr; GDBnnn</i>'; and click <i>Resources &rarr; Download Data</i> in the left menu</li>
	    <li>Or, click the 'Download' icon (<img src="/XGDB/images/download.gif" />) from the appropriate GDB Configuration or Log File page.</li>
	</ul>

<h3>To download files</h3>
	<ul class="bullet1">
	<li>Click the desired directory to view its contents (i.e. <span class="normalfont">| download |	MySQL |	GSQOUT  |	GTHOUT  |	CpGAT  |	BLAST |</span>). Current directory is highlighted.</li>
	<li>Click the desired <b>filename</b> to launch a web browser dialog that will allow you to <b>open</b> or <b>save</b> the file to your local drive. </li>
</ul>
	
